function renderSpriteToCanvas(sprite: SpriteGrid, palette: Record<number, string>): HTMLCanvasElement {
  const size = 24;
  const scale = SPRITE_SCALE;
  const c = document.createElement('canvas');
  c.width = Math.ceil(size * scale);
  c.height = Math.ceil(size * scale);
  const sctx = c.getContext('2d')!;
  for (let y = 0; y < size; y++) {
    for (let x = 0; x < size; x++) {
      const idx = sprite[y][x];
      if (idx === 0) continue;
      sctx.fillStyle = palette[idx];
      sctx.fillRect(Math.floor(x * scale), Math.floor(y * scale), Math.ceil(scale), Math.ceil(scale));
    }
  }
  return c;
}

function getCachedSprite(sprite: SpriteGrid, palette: Record<number, string>): HTMLCanvasElement {
  const key = getSpriteKey(sprite, palette);
  if (!spriteCache[key]) {
    spriteCache[key] = renderSpriteToCanvas(sprite, palette);
  }
  return spriteCache[key];
}

export function getFacing(p: Player): Direction {
  const dx = p.tx - p.x;
  const dy = p.ty - p.y;
  const d = Math.sqrt(dx * dx + dy * dy);
  if (d < 3) return p._lastFacing || 'down';
  const angle = Math.atan2(dy, dx);
  if (angle > -Math.PI / 4 && angle <= Math.PI / 4) return 'right';
  if (angle > Math.PI / 4 && angle <= 3 * Math.PI / 4) return 'down';
  if (angle > -3 * Math.PI / 4 && angle <= -Math.PI / 4) return 'up';
  return 'left';
}

export function getAnimState(p: Player): AnimState {
  if (p._kicking) return 'kick';
  const dx = p.x - (p._prevX ?? p.x);
  const dy = p.y - (p._prevY ?? p.y);
  if (dx * dx + dy * dy > 4) return 'run';
  return 'idle';
}

export function drawPixelPlayer(ctx: CanvasRenderingContext2D, p: Player, palette: Record<number, string>): void {
  const anim = getAnimState(p);
  const facing = getFacing(p);
  p._lastFacing = facing;
  const frames = SPRITES[anim][facing];
  const frameIdx = animFrame % frames.length;
  const sprite = frames[frameIdx];
  const cached = getCachedSprite(sprite, palette);
  const size = 24 * SPRITE_SCALE;
  ctx.drawImage(cached, p.x - size / 2, p.y - size + 6, size, size);
}
