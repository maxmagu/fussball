const ANIM_TICK_RATE = 5;

let animTick = 0;
let animFrame = 0;

export function advanceAnimation(): void {
  animTick++;
  if (animTick >= ANIM_TICK_RATE) {
    animTick = 0;
    animFrame++;
  }
}

export function getAnimFrame(): number {
  return animFrame;
}

function flipH(sprite: SpriteGrid): SpriteGrid {
  return sprite.map(row => [...row].reverse());
}

